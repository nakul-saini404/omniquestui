const ALL_UNIVERSITIES: University[] = [
  ...USA_UNIVERSITIES,
  ...UK_UNIVERSITIES,
  ...CANADA_UNIVERSITIES,
  ...AUSTRALIA_UNIVERSITIES,
  ...GERMANY_UNIVERSITIES,
  ...NETHERLANDS_UNIVERSITIES,
  ...SINGAPORE_UNIVERSITIES,
  ...INDIA_UNIVERSITIES,
  ...JAPAN_UNIVERSITIES,
];

/**
 * Get universities for a specific country filtered by stream.
 */
export function getUnisForCountry(country: Country, stream: Stream): University[] {
  return ALL_UNIVERSITIES.filter(
    (u) =>
      u.country === country &&
      (u.stream.includes(stream) || stream === "Undecided")
  );
}

/**
 * Get all recommended universities across multiple countries.
 */
export function getAllRecommendedUnis(countries: Country[], stream: Stream): University[] {
  return ALL_UNIVERSITIES.filter(
    (u) =>
      countries.includes(u.country) &&
      (u.stream.includes(stream) || stream === "Undecided")
  );
}

/**
 * Get timeline summary for a specific university.
 */
export function getUniTimeline(uniName: string): UniTimeline | undefined {
  return ALL_UNIVERSITIES.find((u) =>
    u.name.toLowerCase().includes(uniName.toLowerCase())
  )?.timeline;
}

/**
 * Returns universities grouped by tier for a given country + stream.
 */
export function getUnisGroupedByTier(
  country: Country,
  stream: Stream
): { reach: University[]; target: University[]; safety: University[] } {
  const unis = getUnisForCountry(country, stream);
  return {
    reach:  unis.filter((u) => u.tier === "Reach"),
    target: unis.filter((u) => u.tier === "Target"),
    safety: unis.filter((u) => u.tier === "Safety"),
  };
}

/**
 * Calculate admission chance based on predicted score vs requirement.
 */
export function calcChance(req: number, predictedFinal: number): { label: string; cls: string } {
  const diff = predictedFinal - req;
  if (diff >= 5)  return { label: "Strong Chance", cls: "chance-green" };
  if (diff >= 0)  return { label: "Good Chance",   cls: "chance-yellow" };
  if (diff >= -5) return { label: "Possible",       cls: "chance-orange" };
  return             { label: "Stretch",            cls: "chance-red" };
}